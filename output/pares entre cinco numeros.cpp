#include <iostream> //biblioteca principal.

using namespace std;

 int main()//função.
   {
     int x;//i= numero de repetições.
     double valor;//a= numero a ser inserido.
     double positivos=0;  //c= começa em 0.
     for( x = 0; x < 6; x++) // calculo de repetições em 6 vezes.
       {
         cin >> valor; //numero a ser inserindo por 6 vezes.
         if(valor > 0)//se o numero for maior que 0 é positivo
              
           {
             positivos++; // adicionar 1
           }
       }
      cout << positivos << " valores positivos" << endl; // responder quantos nmeros oisitivs tem em 5 entradas.
     return 0;
   }